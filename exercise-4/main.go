package main

import "fmt"

func main() {
	var sobrenome string = "Silva"
	var idade int = 25
	boolean := false
	var salario float32 = 4585.90
	var nome string = "Fellipe"

	fmt.Println("sobrenome", sobrenome)
	fmt.Println("var idade int = \"25\" -> var idade int = 25", idade)
	fmt.Println("boolean := \"false\"; -> boolean := false", boolean)
	fmt.Println("var salario string = 4585.90 -> var salario float32 = 4585.90", salario)
	fmt.Println("nome", nome)
}
